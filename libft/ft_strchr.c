#include "libft.h"

char	*ft_strchr(const char *str, int n)
{
	char	*s;

	s = (char *)str;
	while (*s)
	{
		if (*s == n)
			return (s);
		s++;
	}
	if (n == 0)
		return (s);
	return (NULL);
}
